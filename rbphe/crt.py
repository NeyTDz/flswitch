import gmpy2
from timeit import default_timer as timer


def CRT(residue_list, prime_list):
    # if len(residue_list) != len(prime_list):
    #     raise ValueError("number of residues is not equal to number of primes!")
    N = 1
    for p in prime_list:
        N = gmpy2.mul(N, p)
    result = 0
    for i in range(len(prime_list)):
        Mi = gmpy2.div(N, prime_list[i])
        inv_Mi = gmpy2.invert(Mi, prime_list[i])
        uni = gmpy2.mul(residue_list[i], Mi)
        uni = gmpy2.mul(uni, inv_Mi)
        result = gmpy2.add(result, uni)
    return int(gmpy2.f_mod(result, N))


